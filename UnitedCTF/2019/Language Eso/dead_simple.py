string = '++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++S++++++S-----------S++++++S_+++++++++++++++++++++++++++++++++++++++++++++S++++++++++++++++++++++++++++SS_++++++++++++++++++++++++++++++++++++++++++++++S+++S+++++++++++++++S++++++++++++++++S----------------S++++++++++++++++++++++++++++++++++++++++++++++++S_+++++++++++++++++++++++++++++++++++++++++++++++++++S++SS--S'

counter = 0
for c in string:
	if c == '+':
		counter += 1
	elif c == '-':
		counter -= 1
	elif c == '_':
		counter = 0
	elif c == 'S':
		print(chr(counter))